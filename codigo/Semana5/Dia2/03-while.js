//While, primero pregunta y luego ejecuta

let contador = 1;

//while(condicional)
while(contador<=15){
    //si es verdadero
    console.log(`El contador es: ${contador}`);
    contador=contador+1;
}

//do while, primero ejecuta y luego pregunta
let count = 1;

do{
    console.log(`El contador es: ${count}`);
    count = count +1;
}while(count <=0)